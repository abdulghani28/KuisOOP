public class Peserta {
    String nim;
    String nama;
    int nilaiTulis;
    int nilaiCoding;
    int nilaiWawancara;

    public Peserta(String nim, String nama, int nilaiTulis, int nilaiCoding, int nilaiWawancara) {
        this.nim = nim;
        this.nama = nama;
        this.nilaiTulis = nilaiTulis;
        this.nilaiCoding = nilaiCoding;
        this.nilaiWawancara = nilaiWawancara;
    }

}
